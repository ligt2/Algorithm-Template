#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

/*=============================================================
  【背包DP - 01背包 / 完全背包 / 多重背包】
  
  ⭐⭐⭐⭐⭐ 省赛必考，优先级最高
  
  ═══════════════════════════════════════════════════════════
  【什么时候用？】
  
  ✅ 01背包信号词：
     • "每个物品只能选一次"
     • "选或不选"
     • "从n个物品中选若干个"
     • "子集和问题"
  
  ✅ 完全背包信号词：
     • "每个物品可以选无限次"
     • "硬币找零"（硬币数量无限）
     • "物品数量不限"
  
  ✅ 多重背包信号词：
     • "第i个物品最多选k[i]个"
     • "物品有库存限制"
  
  ═══════════════════════════════════════════════════════════
  【题目特征】
  
  • 有 n 个物品，每个物品有"体积/重量"和"价值"两个属性
  • 有一个"背包容量"限制
  • 求：最大价值 / 能否恰好装满 / 方案数
  
  ═══════════════════════════════════════════════════════════
  【复杂度】
  
  • 时间：O(n × W)，n是物品数，W是背包容量
  • 空间：O(W)（滚动数组优化后）
  
  ═══════════════════════════════════════════════════════════
  【典型例题】
  
  • 蓝桥杯：背包问题、砝码称重、数字组合
  • LeetCode：416分割等和子集、322零钱兑换、494目标和
  
  ═══════════════════════════════════════════════════════════
  【核心区别】
  
  ┌──────────────────────────────────────────────────────────┐
  │  类型        内层循环方向    原因                         │
  ├──────────────────────────────────────────────────────────┤
  │  01背包      逆序 (W→w[i])  保证每个物品只用一次          │
  │  完全背包    正序 (w[i]→W)  允许同一物品重复使用          │
  │  多重背包    二进制拆分     转化为01背包                  │
  └──────────────────────────────────────────────────────────┘
  
=============================================================*/

// ═══════════════════════════════════════════════════════════
// 【模板1】01背包 - 求最大价值
// ═══════════════════════════════════════════════════════════
struct Knapsack01 {
    int n;      // 物品数量
    int W;      // 背包容量
    vector<int> dp;  // dp[j] = 容量为j时的最大价值
    
    // 【构造函数】初始化背包
    // 参数：物品数量n，背包容量W
    Knapsack01(int n, int W) : n(n), W(W), dp(W + 1, 0) {}
    
    // 【添加物品】处理第i个物品
    // 参数：物品重量w，物品价值v
    // ⚠️ 修改点：如果求"恰好装满"，初始化dp[0]=0, 其他为-INF
    void add(int w, int v) {
        // 【关键】逆序遍历，保证每个物品只选一次
        // 原理：dp[j-w]还是"未选当前物品"的状态
        for (int j = W; j >= w; j--) {
            // 转移：不选 vs 选当前物品
            dp[j] = max(dp[j], dp[j - w] + v);
        }
    }
    
    // 【查询结果】
    int query() { return dp[W]; }
    
    // 【查询任意容量】
    int query(int capacity) { return dp[capacity]; }
};

// ═══════════════════════════════════════════════════════════
// 【模板2】01背包 - 判断能否恰好装满（可行性）
// ═══════════════════════════════════════════════════════════
struct Knapsack01Exact {
    int W;
    vector<bool> dp;  // dp[j] = 能否恰好凑成容量j
    
    Knapsack01Exact(int W) : W(W), dp(W + 1, false) {
        dp[0] = true;  // 【初始化】容量0一定能凑成（什么都不选）
    }
    
    void add(int w) {
        for (int j = W; j >= w; j--) {
            // 转移：如果dp[j-w]可行，那么选上当前物品后dp[j]也可行
            dp[j] = dp[j] || dp[j - w];
        }
    }
    
    bool query() { return dp[W]; }
    bool query(int target) { return dp[target]; }
};

// ═══════════════════════════════════════════════════════════
// 【模板3】01背包 - 求方案数
// ═══════════════════════════════════════════════════════════
struct Knapsack01Count {
    int W;
    vector<ll> dp;  // dp[j] = 恰好凑成容量j的方案数
    ll MOD;
    
    // 参数：容量W，取模值mod（默认不取模）
    Knapsack01Count(int W, ll mod = LLONG_MAX) : W(W), dp(W + 1, 0), MOD(mod) {
        dp[0] = 1;  // 【初始化】凑成0有1种方案（不选）
    }
    
    void add(int w) {
        for (int j = W; j >= w; j--) {
            dp[j] = (dp[j] + dp[j - w]) % MOD;
        }
    }
    
    ll query() { return dp[W]; }
    ll query(int target) { return dp[target]; }
};

// ═══════════════════════════════════════════════════════════
// 【模板4】完全背包 - 求最大价值
// ═══════════════════════════════════════════════════════════
struct KnapsackComplete {
    int W;
    vector<int> dp;
    
    KnapsackComplete(int W) : W(W), dp(W + 1, 0) {}
    
    void add(int w, int v) {
        // 【关键】正序遍历，允许同一物品重复选
        // 原理：dp[j-w]可能已经包含了当前物品
        for (int j = w; j <= W; j++) {
            dp[j] = max(dp[j], dp[j - w] + v);
        }
    }
    
    int query() { return dp[W]; }
};

// ═══════════════════════════════════════════════════════════
// 【模板5】完全背包 - 最少硬币数（恰好凑成target）
// ═══════════════════════════════════════════════════════════
struct CoinChange {
    int W;
    vector<int> dp;  // dp[j] = 凑成j所需的最少硬币数
    
    CoinChange(int W) : W(W), dp(W + 1, INT_MAX) {
        dp[0] = 0;  // 【初始化】凑成0需要0个硬币
    }
    
    void add(int coin) {
        for (int j = coin; j <= W; j++) {
            if (dp[j - coin] != INT_MAX) {
                dp[j] = min(dp[j], dp[j - coin] + 1);
            }
        }
    }
    
    // 返回-1表示无法凑成
    int query() { return dp[W] == INT_MAX ? -1 : dp[W]; }
    int query(int target) { return dp[target] == INT_MAX ? -1 : dp[target]; }
};

// ═══════════════════════════════════════════════════════════
// 【模板6】多重背包 - 二进制拆分
// ═══════════════════════════════════════════════════════════
struct KnapsackMultiple {
    int W;
    vector<int> dp;
    
    KnapsackMultiple(int W) : W(W), dp(W + 1, 0) {}
    
    // 参数：物品重量w，物品价值v，物品数量k
    void add(int w, int v, int k) {
        // 【二进制拆分】将k个物品拆成若干组，转化为01背包
        // 例：k=13 拆成 1+2+4+6（1,2,4是2的幂，6是剩余）
        for (int t = 1; k > 0; t *= 2) {
            int cnt = min(t, k);  // 这一组的物品数
            k -= cnt;
            int ww = w * cnt;  // 这一组的总重量
            int vv = v * cnt;  // 这一组的总价值
            
            // 当作01背包处理
            for (int j = W; j >= ww; j--) {
                dp[j] = max(dp[j], dp[j - ww] + vv);
            }
        }
    }
    
    int query() { return dp[W]; }
};

// ═══════════════════════════════════════════════════════════
// 【测试用例】
// ═══════════════════════════════════════════════════════════
int main() {
    cout << "===== 背包DP模板测试 =====\n\n";
    
    // ─────────────────────────────────────────────────────
    // 测试1：01背包最大价值
    // 3个物品：重量[2,3,4]，价值[3,4,5]，背包容量5
    // 最优：选物品0和1，总重量5，总价值7
    // ─────────────────────────────────────────────────────
    {
        Knapsack01 bag(3, 5);
        bag.add(2, 3);  // 物品0
        bag.add(3, 4);  // 物品1
        bag.add(4, 5);  // 物品2
        
        assert(bag.query() == 7);
        cout << "[01背包-最大价值] 测试通过，最大价值=" << bag.query() << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试2：01背包判断可行性（能否恰好装满）
    // 数组[1,5,11,5]，能否分成两个和相等的子集？
    // 总和22，每个子集需要和为11
    // 可行：[1,5,5] 和 [11]
    // ─────────────────────────────────────────────────────
    {
        vector<int> nums = {1, 5, 11, 5};
        int sum = 0;
        for (int x : nums) sum += x;
        
        if (sum % 2 == 0) {
            Knapsack01Exact bag(sum / 2);
            for (int x : nums) bag.add(x);
            assert(bag.query() == true);
            cout << "[01背包-分割等和] 测试通过，可以分割\n";
        }
    }
    
    // ─────────────────────────────────────────────────────
    // 测试3：01背包方案数
    // 数组[1,2,3,4,5]，选若干个数使得和为5的方案数
    // 方案：{5}, {1,4}, {2,3} → 共3种
    // ─────────────────────────────────────────────────────
    {
        Knapsack01Count bag(5);
        for (int x : {1, 2, 3, 4, 5}) bag.add(x);
        
        assert(bag.query() == 3);
        cout << "[01背包-方案数] 测试通过，方案数=" << bag.query() << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试4：完全背包
    // 物品：重量[2,3]，价值[3,4]，背包容量7
    // 最优：选3个物品0（重量6，价值9）+ 无法再放 = 9
    // 或者：选2个物品1（重量6，价值8）+ 无法再放 = 8
    // 或者：选2个物品0 + 1个物品1（重量7，价值10）= 10
    // ─────────────────────────────────────────────────────
    {
        KnapsackComplete bag(7);
        bag.add(2, 3);
        bag.add(3, 4);
        
        assert(bag.query() == 10);
        cout << "[完全背包-最大价值] 测试通过，最大价值=" << bag.query() << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试5：硬币找零（最少硬币数）
    // 硬币面值[1,2,5]，凑成11
    // 最优：5+5+1 = 3枚
    // ─────────────────────────────────────────────────────
    {
        CoinChange cc(11);
        cc.add(1);
        cc.add(2);
        cc.add(5);
        
        assert(cc.query() == 3);
        cout << "[完全背包-硬币找零] 测试通过，最少硬币=" << cc.query() << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试6：多重背包
    // 物品0：重量1，价值2，数量3
    // 物品1：重量2，价值4，数量2
    // 背包容量5
    // 最优：3个物品0 + 1个物品1 = 重量5，价值10
    // ─────────────────────────────────────────────────────
    {
        KnapsackMultiple bag(5);
        bag.add(1, 2, 3);  // 重量1，价值2，数量3
        bag.add(2, 4, 2);  // 重量2，价值4，数量2
        
        assert(bag.query() == 10);
        cout << "[多重背包] 测试通过，最大价值=" << bag.query() << "\n";
    }
    
    cout << "\n===== 所有背包DP测试通过 =====\n";
    return 0;
}