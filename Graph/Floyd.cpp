#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
const ll INF = 1e18;

/*=============================================================
  【Floyd算法 - 全源最短路】
  
  ⭐⭐⭐⭐ 省赛常用
  
  ═══════════════════════════════════════════════════════════
  【什么时候用？】
  
  ✅ 明确信号词：
     • "任意两点最短路"
     • "传递闭包"
     • "所有点对之间的距离"
  
  ✅ 使用条件：
     • n ≤ 500（因为O(n³)复杂度）
     • 需要多次查询不同点对的最短路
     • 可以有负权边（但不能有负环）
  
  ✅ 典型场景：
     • 任意两点间最短距离
     • 判断图的连通性（传递闭包）
     • 中转点相关的问题
  
  ═══════════════════════════════════════════════════════════
  【复杂度】
  
  • 时间：O(n³)
  • 空间：O(n²)
  • 适用：n ≤ 500
  
  ═══════════════════════════════════════════════════════════
  【核心原理】
  
  d[i][j] = min(d[i][j], d[i][k] + d[k][j])
  
  尝试以每个点k作为中转点，更新i到j的最短路
  三重循环的顺序必须是 k 在最外层！
  
  ═══════════════════════════════════════════════════════════
  【常见变体/修改点】
  
  1. 传递闭包：d[i][j] = d[i][j] || (d[i][k] && d[k][j])
  2. 记录路径：添加path[i][j]记录中转点
  3. 最小环：枚举时顺便记录
  4. 限制经过的点：修改k的枚举顺序
  
=============================================================*/

struct Floyd {
    int n;                        // 节点数量
    vector<vector<ll>> d;         // d[i][j] = i到j的最短距离
    vector<vector<int>> path;     // path[i][j] = i到j路径上的中转点（可选）

    // ═══════════════════════════════════════════════════════
    // 【构造函数】
    // 参数 n: 节点数量（1-indexed）
    // 
    // 初始化：d[i][i] = 0，其他为INF
    // ═══════════════════════════════════════════════════════
    Floyd(int n) : n(n), d(n + 1, vector<ll>(n + 1, INF)),
                   path(n + 1, vector<int>(n + 1, -1)) {
        for (int i = 0; i <= n; i++) d[i][i] = 0;
    }

    // ═══════════════════════════════════════════════════════
    // 【添加边】
    // 
    // 【修改点】
    // • 无向图：取消下方注释
    // • 多条边取最小：已处理（取min）
    // ═══════════════════════════════════════════════════════
    void add(int u, int v, ll w) {
        d[u][v] = min(d[u][v], w);
        // d[v][u] = min(d[v][u], w);  // 【无向图】取消此注释
    }

    // ═══════════════════════════════════════════════════════
    // 【运行Floyd】
    // 
    // 【核心】三重循环，k必须在最外层
    // 原理：依次考虑是否经过节点1,2,3,...,n作为中转
    // ═══════════════════════════════════════════════════════
    void run() {
        // 【关键】k在最外层！
        // 含义：d[i][j]表示只用前k个点作为中转时，i到j的最短路
        for (int k = 1; k <= n; k++) {
            for (int i = 1; i <= n; i++) {
                for (int j = 1; j <= n; j++) {
                    // 【松弛】尝试用k作为中转点
                    if (d[i][k] < INF && d[k][j] < INF) {  // 防止溢出
                        if (d[i][k] + d[k][j] < d[i][j]) {
                            d[i][j] = d[i][k] + d[k][j];
                            path[i][j] = k;  // 记录中转点
                        }
                    }
                }
            }
        }
    }

    // ═══════════════════════════════════════════════════════
    // 【查询最短距离】
    // ═══════════════════════════════════════════════════════
    ll query(int u, int v) { 
        return d[u][v]; 
    }

    // ═══════════════════════════════════════════════════════
    // 【获取路径】
    // 返回从u到v的最短路径（节点序列）
    // ═══════════════════════════════════════════════════════
    vector<int> getPath(int u, int v) {
        if (d[u][v] == INF) return {};
        
        vector<int> result;
        buildPath(u, v, result);
        return result;
    }

    void buildPath(int u, int v, vector<int>& result) {
        if (path[u][v] == -1) {
            // 没有中转点，直接相连
            if (result.empty() || result.back() != u) {
                result.push_back(u);
            }
            if (u != v) result.push_back(v);
        } else {
            int k = path[u][v];
            buildPath(u, k, result);
            buildPath(k, v, result);
        }
    }

    // ═══════════════════════════════════════════════════════
    // 【判断负环】
    // 如果d[i][i] < 0，说明存在经过i的负环
    // ═══════════════════════════════════════════════════════
    bool hasNegativeCycle() {
        for (int i = 1; i <= n; i++) {
            if (d[i][i] < 0) return true;
        }
        return false;
    }
};

// ═══════════════════════════════════════════════════════════
// 【变体】传递闭包
// ═══════════════════════════════════════════════════════════
struct TransitiveClosure {
    int n;
    vector<vector<bool>> reach;  // reach[i][j] = i能否到达j

    TransitiveClosure(int n) : n(n), reach(n + 1, vector<bool>(n + 1, false)) {
        for (int i = 0; i <= n; i++) reach[i][i] = true;
    }

    void add(int u, int v) {
        reach[u][v] = true;
    }

    void run() {
        for (int k = 1; k <= n; k++) {
            for (int i = 1; i <= n; i++) {
                for (int j = 1; j <= n; j++) {
                    // 【关键】如果i能到k，k能到j，那么i能到j
                    reach[i][j] = reach[i][j] || (reach[i][k] && reach[k][j]);
                }
            }
        }
    }

    bool canReach(int u, int v) { return reach[u][v]; }
};

// ═══════════════════════════════════════════════════════════
// 【测试用例】
// ═══════════════════════════════════════════════════════════
int main() {
    cout << "===== Floyd模板测试 =====\n\n";
    
    // ─────────────────────────────────────────────────────
    // 测试1：基础全源最短路
    // 图：1→2(1), 2→3(2), 1→3(10)
    // ─────────────────────────────────────────────────────
    {
        Floyd fl(3);
        fl.add(1, 2, 1);
        fl.add(2, 3, 2);
        fl.add(1, 3, 10);
        fl.run();
        
        assert(fl.query(1, 3) == 3);
        assert(fl.query(1, 2) == 1);
        
        cout << "[Floyd] 测试通过\n";
        cout << "  d(1,3) = " << fl.query(1, 3) << "\n";
        cout << "  d(1,2) = " << fl.query(1, 2) << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试2：传递闭包
    // ─────────────────────────────────────────────────────
    {
        TransitiveClosure tc(3);
        tc.add(1, 2);
        tc.add(2, 3);
        tc.run();
        
        assert(tc.canReach(1, 3) == true);
        assert(tc.canReach(3, 1) == false);
        
        cout << "[传递闭包] 测试通过\n";
        cout << "  1能到3: " << (tc.canReach(1,3) ? "是" : "否") << "\n";
    }
    
    cout << "\n===== Floyd测试通过 =====\n";
    return 0;
}