#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

/*=============================================================
  【Kruskal算法 - 最小生成树】
  
  ⭐⭐⭐⭐ 省赛常用
  
  ═══════════════════════════════════════════════════════════
  【什么时候用？】
  
  ✅ 信号词：
     • "最小生成树"、"MST"
     • "连接所有点的最小代价"
     • "最小总边权使图连通"
     • "最小通信网络"
  
  ✅ 典型场景：
     • 修建公路连接所有城市的最小花费
     • 网络布线最小成本
     • 需要选n-1条边连接n个点
  
  ═══════════════════════════════════════════════════════════
  【与Prim对比】
  
  ┌──────────────────────────────────────────────────────────┐
  │  算法      复杂度           适用场景                      │
  ├──────────────────────────────────────────────────────────┤
  │  Kruskal  O(m log m)       稀疏图（边数不多）            │
  │  Prim     O(n² 或 m log n) 稠密图（边数接近n²）          │
  └──────────────────────────────────────────────────────────┘
  
  ═══════════════════════════════════════════════════════════
  【核心原理】
  
  1. 将所有边按权值从小到大排序
  2. 依次考虑每条边，如果两端点不在同一连通分量，就选这条边
  3. 选够n-1条边就停止
  
  ═══════════════════════════════════════════════════════════
  【常见变体/修改点】
  
  1. 最大生成树：排序时从大到小
  2. 次小生成树：需要额外处理
  3. 判断MST是否唯一：检查是否有相同权值的替代边
  4. 限制某些边必选/必不选：调整加边顺序
  
=============================================================*/

// ═══════════════════════════════════════════════════════════
// 【并查集】Kruskal必需的数据结构
// ═══════════════════════════════════════════════════════════
struct DSU {
    vector<int> fa, sz;
    
    DSU(int n) : fa(n + 1), sz(n + 1, 1) { 
        iota(fa.begin(), fa.end(), 0); 
    }
    
    int find(int x) { 
        return fa[x] == x ? x : fa[x] = find(fa[x]); 
    }
    
    bool merge(int x, int y) {
        x = find(x); y = find(y);
        if (x == y) return false;
        if (sz[x] < sz[y]) swap(x, y);
        fa[y] = x; sz[x] += sz[y];
        return true;
    }
};

// ═══════════════════════════════════════════════════════════
// 【Kruskal最小生成树】
// ═══════════════════════════════════════════════════════════
struct Kruskal {
    int n;                    // 节点数量
    
    // 【边的结构】
    struct Edge { 
        int u, v;             // 两个端点
        ll w;                 // 边权
        
        // 按边权从小到大排序
        bool operator<(const Edge &o) const { 
            return w < o.w; 
        }
    };
    
    vector<Edge> edges;       // 所有边
    vector<Edge> mstEdges;    // MST中选中的边

    // ═══════════════════════════════════════════════════════
    // 【构造函数】
    // 参数 n: 节点数量（1-indexed）
    // ═══════════════════════════════════════════════════════
    Kruskal(int n) : n(n) {}

    // ═══════════════════════════════════════════════════════
    // 【添加边】
    // ═══════════════════════════════════════════════════════
    void add(int u, int v, ll w) { 
        edges.push_back({u, v, w}); 
    }

    // ═══════════════════════════════════════════════════════
    // 【运行Kruskal】
    // 返回值：{MST总权值, MST边数}
    // 
    // 如果 边数 < n-1，说明图不连通，无法形成生成树
    // ═══════════════════════════════════════════════════════
    pair<ll, int> run() {
        // 【步骤1】按边权排序
        sort(edges.begin(), edges.end());
        
        // 【步骤2】用并查集维护连通性
        DSU dsu(n);
        
        ll sum = 0;   // MST总权值
        int cnt = 0;  // 已选边数
        mstEdges.clear();
        
        // 【步骤3】依次考虑每条边
        for (auto &e : edges) {
            // 如果两端点不在同一连通分量
            if (dsu.merge(e.u, e.v)) {
                sum += e.w;
                cnt++;
                mstEdges.push_back(e);
                
                // 【优化】选够n-1条边就停止
                if (cnt == n - 1) break;
            }
        }
        
        return {sum, cnt};
    }

    // ═══════════════════════════════════════════════════════
    // 【获取MST的边】
    // ═══════════════════════════════════════════════════════
    vector<Edge>& getMSTEdges() {
        return mstEdges;
    }
};

// ═══════════════════════════════════════════════════════════
// 【变体】最大生成树
// ═══════════════════════════════════════════════════════════
struct KruskalMax {
    int n;
    struct Edge { 
        int u, v; ll w;
        bool operator<(const Edge &o) const { 
            return w > o.w;  // 【关键】从大到小排序
        }
    };
    vector<Edge> edges;

    KruskalMax(int n) : n(n) {}
    void add(int u, int v, ll w) { edges.push_back({u, v, w}); }

    pair<ll, int> run() {
        sort(edges.begin(), edges.end());
        DSU dsu(n);
        ll sum = 0; int cnt = 0;
        for (auto &[u, v, w] : edges) {
            if (dsu.merge(u, v)) {
                sum += w; cnt++;
                if (cnt == n - 1) break;
            }
        }
        return {sum, cnt};
    }
};

// ═══════════════════════════════════════════════════════════
// 【测试用例】
// ═══════════════════════════════════════════════════════════
int main() {
    cout << "===== Kruskal模板测试 =====\n\n";
    
    // ─────────────────────────────────────────────────────
    // 测试1：基础MST
    // 4个点，边：1-2(1), 2-3(2), 3-4(3), 1-4(10), 1-3(5)
    // MST：1-2(1), 2-3(2), 3-4(3)，总权值6
    // ─────────────────────────────────────────────────────
    {
        Kruskal kr(4);
        kr.add(1, 2, 1);
        kr.add(2, 3, 2);
        kr.add(3, 4, 3);
        kr.add(1, 4, 10);
        kr.add(1, 3, 5);
        
        auto [sum, cnt] = kr.run();
        
        assert(sum == 6);
        assert(cnt == 3);
        
        cout << "[Kruskal] 测试通过\n";
        cout << "  MST总权值 = " << sum << "\n";
        cout << "  MST边数 = " << cnt << "\n";
        cout << "  MST的边: ";
        for (auto &e : kr.getMSTEdges()) {
            cout << "(" << e.u << "-" << e.v << ":" << e.w << ") ";
        }
        cout << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试2：图不连通
    // ─────────────────────────────────────────────────────
    {
        Kruskal kr(4);
        kr.add(1, 2, 1);
        // 3和4是孤立的
        
        auto [sum, cnt] = kr.run();
        
        assert(cnt < 3);  // 无法形成生成树
        cout << "[图不连通] 测试通过，只能选" << cnt << "条边\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试3：最大生成树
    // ─────────────────────────────────────────────────────
    {
        KruskalMax kr(4);
        kr.add(1, 2, 1);
        kr.add(2, 3, 2);
        kr.add(3, 4, 3);
        kr.add(1, 4, 10);
        kr.add(1, 3, 5);
        
        auto [sum, cnt] = kr.run();
        
        // 最大生成树：1-4(10), 1-3(5), 2-3(2)，总权值17
        assert(sum == 17);
        cout << "[最大生成树] 测试通过，总权值=" << sum << "\n";
    }
    
    cout << "\n===== Kruskal测试通过 =====\n";
    return 0;
}