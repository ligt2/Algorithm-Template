#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
const ll INF = 1e18;

/*=============================================================
  【Dijkstra算法 - 单源最短路】
  
  ⭐⭐⭐⭐⭐ 省赛必会，最常用的最短路算法
  
  ═══════════════════════════════════════════════════════════
  【什么时候用？】
  
  ✅ 信号词：
     • "最短路"、"最短距离"、"最少花费"
     • "从起点到各点的最短路"
     • "单源最短路"
  
  ✅ 使用条件：
     • 边权非负（有负权边用SPFA/Bellman-Ford）
     • 求一个源点到其他所有点的最短距离
  
  ✅ 典型场景：
     • 地图导航最短路径
     • 网络延迟最小化
     • 最小花费问题
  
  ═══════════════════════════════════════════════════════════
  【复杂度】
  
  • 优先队列版：O((V+E) log V)
  • 适用规模：V, E ≤ 10^5 ~ 10^6
  
  ═══════════════════════════════════════════════════════════
  【常见变体/修改点】
  
  1. 无向图：add函数中取消注释反向边
  2. 多源最短路：添加虚拟超级源点，或多次调用run
  3. 求路径：添加pre数组记录前驱
  4. 次短路：记录最短和次短两个值
  5. 限制边数的最短路：加一个状态维度
  
  ═══════════════════════════════════════════════════════════
  【与其他最短路算法对比】
  
  ┌──────────────────────────────────────────────────────────┐
  │  算法        边权要求      复杂度          适用场景       │
  ├──────────────────────────────────────────────────────────┤
  │  Dijkstra   非负         O((V+E)logV)    稀疏图单源      │
  │  Floyd      任意         O(V³)           全源，V≤500    │
  │  SPFA       任意         O(VE)最坏       有负权/判负环   │
  │  Bellman    任意         O(VE)           有负权/判负环   │
  └──────────────────────────────────────────────────────────┘
  
=============================================================*/

struct Dijkstra {
    int n;                                    // 节点数量
    vector<vector<pair<int, int>>> g;         // 邻接表：g[u] = {(v, w), ...}
    vector<ll> dist;                          // dist[u] = 从源点到u的最短距离
    vector<int> pre;                          // pre[u] = 最短路上u的前驱（可选）

    // ═══════════════════════════════════════════════════════
    // 【构造函数】
    // 参数 n: 节点数量（1-indexed，节点编号1~n）
    // ═══════════════════════════════════════════════════════
    Dijkstra(int n) : n(n), g(n + 1), dist(n + 1, INF), pre(n + 1, -1) {}

    // ═══════════════════════════════════════════════════════
    // 【添加边】
    // 参数 u, v: 边的两个端点
    // 参数 w: 边权（必须非负！）
    // 
    // 【修改点】
    // • 无向图：取消下方注释，添加反向边
    // • 多条边取最小：可以不处理，Dijkstra会自动选最优
    // ═══════════════════════════════════════════════════════
    void add(int u, int v, int w) {
        g[u].emplace_back(v, w);
        // g[v].emplace_back(u, w);  // 【无向图】取消此注释
    }

    // ═══════════════════════════════════════════════════════
    // 【运行Dijkstra】
    // 参数 s: 源点
    // 
    // 【核心原理】
    // 每次从未确定的点中，选择距离最小的点u，
    // 用u去更新它能到达的所有点v的距离
    // ═══════════════════════════════════════════════════════
    void run(int s) {
        // 【初始化】所有距离设为无穷大
        fill(dist.begin(), dist.end(), INF);
        fill(pre.begin(), pre.end(), -1);
        
        dist[s] = 0;  // 源点到自己距离为0
        
        // 【优先队列】小根堆，按距离排序
        // pair<距离, 节点编号>，距离小的优先
        priority_queue<pair<ll,int>, vector<pair<ll,int>>, greater<>> pq;
        pq.push({0, s});
        
        while (!pq.empty()) {
            // 【取出当前距离最小的点】
            auto [d, u] = pq.top(); 
            pq.pop();
            
            // 【剪枝】如果这个距离已经过时（被更优的更新过），跳过
            // 这是因为我们可能多次将同一个点入队
            if (d > dist[u]) continue;
            
            // 【松弛操作】尝试用u更新它所有邻居的距离
            for (auto &[v, w] : g[u]) {
                // 如果经过u到v更短，就更新
                if (dist[u] + w < dist[v]) {
                    dist[v] = dist[u] + w;
                    pre[v] = u;           // 记录前驱（可选）
                    pq.push({dist[v], v}); // 将新距离入队
                }
            }
        }
    }

    // ═══════════════════════════════════════════════════════
    // 【查询距离】
    // 返回从源点到u的最短距离
    // 如果返回INF，说明u从源点不可达
    // ═══════════════════════════════════════════════════════
    ll query(int u) { 
        return dist[u]; 
    }

    // ═══════════════════════════════════════════════════════
    // 【获取最短路径】（需要先run）
    // 返回从源点到t的路径（节点序列）
    // ═══════════════════════════════════════════════════════
    vector<int> getPath(int t) {
        vector<int> path;
        if (dist[t] == INF) return path;  // 不可达
        
        for (int u = t; u != -1; u = pre[u]) {
            path.push_back(u);
        }
        reverse(path.begin(), path.end());
        return path;
    }
};

// ═══════════════════════════════════════════════════════════
// 【测试用例】
// ═══════════════════════════════════════════════════════════
int main() {
    cout << "===== Dijkstra模板测试 =====\n\n";
    
    // ─────────────────────────────────────────────────────
    // 测试1：基础最短路
    // 图：1→2(1), 2→3(2), 1→3(10)
    // 1到3的最短路：1→2→3 = 3
    // ─────────────────────────────────────────────────────
    {
        Dijkstra dij(3);
        dij.add(1, 2, 1);
        dij.add(2, 3, 2);
        dij.add(1, 3, 10);
        dij.run(1);
        
        assert(dij.query(3) == 3);
        assert(dij.query(2) == 1);
        
        cout << "[基础最短路] 测试通过\n";
        cout << "  dist(1→2) = " << dij.query(2) << "\n";
        cout << "  dist(1→3) = " << dij.query(3) << "\n";
        
        auto path = dij.getPath(3);
        cout << "  路径: ";
        for (int v : path) cout << v << " ";
        cout << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试2：不可达的情况
    // ─────────────────────────────────────────────────────
    {
        Dijkstra dij(3);
        dij.add(1, 2, 1);
        // 没有边到3
        dij.run(1);
        
        assert(dij.query(3) == INF);
        cout << "[不可达] 测试通过，dist(1→3) = INF\n";
    }
    
    cout << "\n===== Dijkstra测试通过 =====\n";
    return 0;
}