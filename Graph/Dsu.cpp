#include <bits/stdc++.h>
using namespace std;

/*=============================================================
  【并查集 (Disjoint Set Union / Union-Find)】
  
  ⭐⭐⭐⭐⭐ 省赛必会，基础中的基础
  
  ═══════════════════════════════════════════════════════════
  【什么时候用？】
  
  ✅ 信号词：
     • "连通性"、"是否在同一集合"
     • "合并"、"联通"
     • "分组"、"朋友圈"
     • "最小生成树"（Kruskal需要）
  
  ✅ 典型场景：
     • 判断两点是否连通
     • 动态添加边，维护连通分量
     • 求连通分量个数
     • Kruskal最小生成树
  
  ═══════════════════════════════════════════════════════════
  【复杂度】
  
  • 路径压缩 + 按秩合并：均摊 O(α(n)) ≈ O(1)
  • α(n) 是阿克曼函数的反函数，增长极慢
  
  ═══════════════════════════════════════════════════════════
  【常见变体/修改点】
  
  1. 带权并查集：维护到根的距离/关系
  2. 可撤销并查集：用栈记录操作，支持撤销
  3. 扩展域并查集：处理"敌人的敌人是朋友"类问题
  4. 记录集合元素：每个集合维护一个vector
  
=============================================================*/

struct DSU {
    vector<int> fa;   // fa[x] = x的父节点
    vector<int> sz;   // sz[x] = 以x为根的集合大小（仅根节点有效）

    // ═══════════════════════════════════════════════════════
    // 【构造函数】
    // 参数 n: 元素数量（1-indexed，元素编号1~n）
    // 
    // 初始状态：每个元素自成一个集合，fa[i] = i
    // ═══════════════════════════════════════════════════════
    DSU(int n) : fa(n + 1), sz(n + 1, 1) { 
        // iota: 将fa填充为 0, 1, 2, ..., n
        iota(fa.begin(), fa.end(), 0); 
    }

    // ═══════════════════════════════════════════════════════
    // 【查找根节点】
    // 返回x所在集合的代表元素（根节点）
    // 
    // 【路径压缩】递归过程中，将路径上所有节点直接连到根
    // 这样下次查找更快
    // ═══════════════════════════════════════════════════════
    int find(int x) { 
        // 如果x是根（fa[x] == x），直接返回
        // 否则，递归找根，并将fa[x]直接设为根（路径压缩）
        return fa[x] == x ? x : fa[x] = find(fa[x]); 
    }

    // ═══════════════════════════════════════════════════════
    // 【合并两个集合】
    // 将x所在集合与y所在集合合并
    // 返回值：true表示成功合并，false表示已经在同一集合
    // 
    // 【按秩合并】小集合并入大集合，保持树的平衡
    // ═══════════════════════════════════════════════════════
    bool merge(int x, int y) {
        x = find(x);  // 找到x的根
        y = find(y);  // 找到y的根
        
        if (x == y) return false;  // 已经在同一集合
        
        // 【按秩合并】让小的合并到大的
        if (sz[x] < sz[y]) swap(x, y);
        
        fa[y] = x;        // y的根指向x
        sz[x] += sz[y];   // 更新x集合的大小
        
        return true;
    }

    // ═══════════════════════════════════════════════════════
    // 【判断是否在同一集合】
    // ═══════════════════════════════════════════════════════
    bool same(int x, int y) { 
        return find(x) == find(y); 
    }

    // ═══════════════════════════════════════════════════════
    // 【获取集合大小】
    // 返回x所在集合的元素个数
    // ═══════════════════════════════════════════════════════
    int size(int x) { 
        return sz[find(x)]; 
    }

    // ═══════════════════════════════════════════════════════
    // 【获取连通分量数】
    // 返回当前有多少个不同的集合
    // ═══════════════════════════════════════════════════════
    int countSets() {
        int cnt = 0;
        for (int i = 1; i < (int)fa.size(); i++) {
            if (fa[i] == i) cnt++;
        }
        return cnt;
    }
};

// ═══════════════════════════════════════════════════════════
// 【扩展】带权并查集（维护到根的距离）
// ═══════════════════════════════════════════════════════════
struct WeightedDSU {
    vector<int> fa;
    vector<long long> d;  // d[x] = x到fa[x]的距离

    WeightedDSU(int n) : fa(n + 1), d(n + 1, 0) {
        iota(fa.begin(), fa.end(), 0);
    }

    // 【查找】同时维护到根的距离
    pair<int, long long> find(int x) {
        if (fa[x] == x) return {x, 0};
        auto [root, dist] = find(fa[x]);
        fa[x] = root;
        d[x] += dist;  // 路径压缩时累加距离
        return {root, d[x]};
    }

    // 【合并】设置x和y的关系：dist(x) - dist(y) = w
    bool merge(int x, int y, long long w) {
        auto [rx, dx] = find(x);
        auto [ry, dy] = find(y);
        if (rx == ry) {
            return dx - dy == w;  // 检查一致性
        }
        fa[rx] = ry;
        d[rx] = dy - dx + w;
        return true;
    }

    // 【查询距离差】
    // 返回dist(x) - dist(y)，如果不在同一集合返回LLONG_MIN
    long long queryDiff(int x, int y) {
        auto [rx, dx] = find(x);
        auto [ry, dy] = find(y);
        if (rx != ry) return LLONG_MIN;
        return dx - dy;
    }
};

// ═══════════════════════════════════════════════════════════
// 【测试用例】
// ═══════════════════════════════════════════════════════════
int main() {
    cout << "===== 并查集模板测试 =====\n\n";
    
    // ─────────────────────────────────────────────────────
    // 测试1：基础合并和查询
    // ─────────────────────────────────────────────────────
    {
        DSU dsu(5);
        
        dsu.merge(1, 2);
        dsu.merge(3, 4);
        dsu.merge(2, 3);
        
        assert(dsu.same(1, 4) == true);
        assert(dsu.same(1, 5) == false);
        assert(dsu.size(1) == 4);
        
        cout << "[基础并查集] 测试通过\n";
        cout << "  1和4在同一集合: " << (dsu.same(1,4) ? "是" : "否") << "\n";
        cout << "  1所在集合大小: " << dsu.size(1) << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试2：连通分量数
    // ─────────────────────────────────────────────────────
    {
        DSU dsu(5);
        dsu.merge(1, 2);
        dsu.merge(3, 4);
        
        assert(dsu.countSets() == 3);  // {1,2}, {3,4}, {5}
        cout << "[连通分量数] 测试通过，分量数=" << dsu.countSets() << "\n";
    }
    
    // ─────────────────────────────────────────────────────
    // 测试3：带权并查集
    // ─────────────────────────────────────────────────────
    {
        WeightedDSU dsu(3);
        
        // 设置 dist(1) - dist(2) = 5
        // 设置 dist(2) - dist(3) = 3
        dsu.merge(1, 2, 5);
        dsu.merge(2, 3, 3);
        
        // 那么 dist(1) - dist(3) = 8
        assert(dsu.queryDiff(1, 3) == 8);
        cout << "[带权并查集] 测试通过，diff(1,3)=" << dsu.queryDiff(1, 3) << "\n";
    }
    
    cout << "\n===== 并查集测试通过 =====\n";
    return 0;
}